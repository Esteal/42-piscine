void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	b;

	i = 0;
	j = size - 1;
	while (i < size / 2)
	{
		b = tab[i];
		tab[i] = tab[j];
		tab[j] = b;
		i++;
		j--;
	}
}
