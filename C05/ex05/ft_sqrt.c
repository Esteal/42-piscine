int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	i = 1;
	n = 0;
	if (nb == 1)
		return (1);
	while (i <= nb / 2 && i < 46341)
	{
		n = i * i;
		if (n == nb)
			return (i);
		i++;
	}
	return (0);
}
